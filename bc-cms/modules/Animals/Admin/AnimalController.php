    public function index(Request $request)
    {
        $this->checkPermission('animal_view');
        $query = $this->animal::query();
        $query->orderBy('id', 'desc');

        if (!empty($s = $request->input('s'))) {
            $query->where('title', 'LIKE', '%' . $s . '%');
            $query->orderBy('title', 'asc');
        }
        if (!empty($location_id = $request->query('location_id'))) {
            $query->where('location_id', $location_id);
        }
        if ($this->hasPermission('animal_manage_others')) {
            if (!empty($author = $request->input('vendor_id'))) {
                $query->where('author_id', $author);
            }
        } else {
            $query->where('author_id', Auth::id());
        }
        $data = [
            'rows'              => $query->with(['author'])->paginate(20),
            'animal_manage_others' => $this->hasPermission('animal_manage_others'),
            'breadcrumbs'       => [
                [
                    'name' => __('Animals'),
                    'url'  => route('animal.admin.index')
                ],
                [
                    'name'  => __('All'),
                    'class' => 'active'
                ],
            ],
            'page_title'        => __("Animal Management")
        ];

        return view('Animals::admin.index', $data);
    }

