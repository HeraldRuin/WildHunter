                    'name' => __('Animal'),
                    'url'  => route('animal.admin.index')
                ],
                [
                    'name'  => __('Edit Animal'),
                    'class' => 'active'
                ],
            ],
            'page_title'        => __("Edit: :name", ['name' => $row->title])
        ];
        return view('Animals::admin.detail', $data);
    }

    public function store(Request $request, $id)
    {
        if(is_demo_mode()){
            return redirect()->back()->with('danger',__("DEMO MODE: can not add data"));
        }

        if ($id > 0) {

            $this->checkPermission('animal_update');
            $row = $this->animal::find($id);

            if (empty($row)) {
                return redirect(route('animal.admin.index'));
            }
            if ($row->author_id != Auth::id() and !$this->hasPermission('animal_manage_others')) {
                return redirect(route('animal.admin.index'));
            }
        } else {
            $this->checkPermission('animal_create');
            $row = new $this->animal();
            $row->status = "publish";
            // Устанавливаем автора по умолчанию, если нет разрешения на управление другими
            if (!$this->hasPermission('animal_manage_others')) {
                $row->author_id = Auth::id();
            }
        }
        $dataKeys = [
            'title',
            'content',
            'price',
            'is_instant',
            'status',
            'video',
            'faqs',
            'image_id',
            'banner_image_id',
            'gallery',
            'location_id',
            'address',
            'map_lat',
            'map_lng',
            'map_zoom',
            'number',
            'is_featured',
            'default_state',
            'enable_service_fee',
            'ical_import_url'
        ];
        if ($this->hasPermission('animal_manage_others')) {
            $dataKeys[] = 'author_id';
        }
        $row->fillByAttr($dataKeys, $request->input());
        if ($request->input('slug')) {
            $row->slug = $request->input('slug');
        }

        $res = $row->saveOriginOrTranslation($request->input('lang'), true);

        if ($res) {
            // Обновляем объект после сохранения, чтобы получить актуальный ID
            $row->refresh();

            if (!$request->input('lang') or is_default_lang($request->input('lang'))) {
//                $this->saveTerms($row, $request);
                // Сохраняем трофеи только если у животного есть ID
                if ($row->id) {
                    try {
                        $this->saveTrophies($row, $request);
                        $this->saveFines($row, $request);
                        $this->savePreparations($row, $request);
                    } catch (\Exception $e) {
                        \Log::error('Error saving trophies: ' . $e->getMessage());
                    }
                }
            }
            do_action(Hook::AFTER_SAVING,$row,$request);

            if ($id > 0) {
                event(new UpdatedServiceEvent($row));
                return back()->with('success', __('Animal updated'));
            } else {
                event(new CreatedServicesEvent($row));
                return redirect(route('animal.admin.edit', ['id' => $row->id]))->with('success', __('Animal created'));
            }
        }

        return back()->with('error', __('Error saving animal. Please check the data and try again.'));
    }

