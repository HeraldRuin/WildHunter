    public function store( Request $request, $id ){
        if(is_demo_mode()){
            return redirect()->back()->with('danger',__("DEMO MODE: can not add data"));
        }
        if($id>0){
            $this->checkPermission('animal_update');
            $row = $this->animalClass::find($id);
            if (empty($row)) {
                return redirect(route('animal.vendor.index'));
            }

            if($row->author_id != Auth::id() and !$this->hasPermission('animal_manage_others'))
            {
                return redirect(route('animal.vendor.index'));
            }
        }else{
            $this->checkPermission('animal_create');
            $row = new $this->animalClass();
            $row->status = "publish";
            if(setting_item("animal_vendor_create_service_must_approved_by_admin", 0)){
                $row->status = "pending";
            }
            $row->author_id = Auth::id();
        }
        $dataKeys = [
            'title',
            'content',
            'price',
            'is_instant',
            'video',
            'faqs',
            'image_id',
            'banner_image_id',
            'gallery',
            'location_id',
            'address',
            'map_lat',
            'map_lng',
            'map_zoom',
            'number',
            'price',
//            'enable_extra_price',
//            'extra_price',
            'is_featured',
            'default_state',
//            'enable_service_fee',
//            'service_fee',
//            'min_day_before_booking',
//            'min_day_stays',
            'ical_import_url'
        ];

        $row->fillByAttr($dataKeys,$request->input());

//        if(!auth()->user()->checkUserPlan() and $row->status == "publish") {
//            return redirect(route('user.plan'));
//        }

        $res = $row->saveOriginOrTranslation($request->input('lang'),true);

        if ($res) {
            if(!$request->input('lang') or is_default_lang($request->input('lang'))) {
//                $this->saveTerms($row, $request);
            }
            do_action(Hook::AFTER_SAVING,$row,$request);
            if($id > 0 ){
                event(new UpdatedServiceEvent($row));
                return back()->with('success',  __('Animal updated') );
            }else{
                event(new CreatedServicesEvent($row));
                return redirect(route('animal.vendor.edit',['id'=>$row->id]))->with('success', __('Animal created') );
            }
        }
    }

    public function saveTerms($row, $request)
    {
        if (empty($request->input('terms'))) {
            $this->animalTermClass::where('target_id', $row->id)->delete();
        } else {
            $term_ids = $request->input('terms');
            foreach ($term_ids as $term_id) {
                $this->animalClass::firstOrCreate([
                    'term_id' => $term_id,
                    'target_id' => $row->id
                ]);
            }
            $this->animalTermClass::where('target_id', $row->id)->whereNotIn('term_id', $term_ids)->delete();
        }
    }

    public function edit(Request $request, $id)
    {
        $this->checkPermission('animal_update');
        $user_id = Auth::id();
        $row = $this->animalClass::where("author_id", $user_id);
