                        $baseNumber = (int)$customDates[$dateKey]->number;
                    }

                    // Ð¡Ñ‡Ð¸Ñ‚Ð°ÐµÐ¼ Ð·Ð°Ð½ÑÑ‚Ð¾ÑÑ‚ÑŒ Ñ‡ÐµÑ€ÐµÐ· Ð±Ñ€Ð¾Ð½Ð¸
                    $occupied = 0;
