        }

        $list = new \Illuminate\Pagination\LengthAwarePaginator(
            $hotelsCollection,
            $hotelsCollection->count(),
            $list->perPage(),
            $list->currentPage(),
            [
                'path' => request()->url(),
                'query' => request()->query(),
            ]
        );

        $markers = [];
        if ($for_map and !empty($list)) {
            foreach ($list as $row) {
                $markers[] = [
                    "id"      => $row->id,
                    "title"   => $row->title,
                    "lat"     => (float)$row->map_lat,
                    "lng"     => (float)$row->map_lng,
                    "gallery" => $row->getGallery(true),
