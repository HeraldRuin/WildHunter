    public function store(Request $request, $id)
    {
        if(is_demo_mode()){
            return redirect()->back()->with('danger',__("DEMO MODE: can not add data"));
        }

        if ($id > 0) {

//            $this->checkPermission('animal_update');
            $row = $this->weaponType::find($id);

            if (empty($row)) {
                return redirect(route('weapon.admin.index'));
            }
            if ($row->author_id != Auth::id() and !$this->hasPermission('animal_manage_others')) {
                return redirect(route('weapon.admin.index'));
            }
        } else {
//            $this->checkPermission('animal_create');
            $row = new $this->weaponType();
            $row->status = "publish";
        }
        $dataKeys = [
            'title',
            'content',
            'status',
            'video',
            'faqs',
            'image_id',
            'banner_image_id',
            'gallery',
            'location_id',
            'address',
            'map_lat',
            'map_lng',
            'map_zoom',
            'number',
            'is_featured',
            'default_state',
            'ical_import_url'
        ];
        if ($this->hasPermission('animal_manage_others')) {
            $dataKeys[] = 'author_id';
        }
        $row->fillByAttr($dataKeys, $request->input());
        if ($request->input('slug')) {
            $row->slug = $request->input('slug');
        }

        $res = $row->saveOriginOrTranslation($request->input('lang'), true);


        if ($res) {
            if (!$request->input('lang') or is_default_lang($request->input('lang'))) {
//                $this->saveTerms($row, $request);
            }
//            do_action(Hook::AFTER_SAVING,$row,$request);

            if ($id > 0) {
//                event(new UpdatedServiceEvent($row));
                return back()->with('success', __('Weapon updated'));
            } else {
//                event(new CreatedServicesEvent($row));
                return redirect(route('weapon.admin.edit', 2))->with('success', __('Weapon created'));
            }
        }
    }
    public function edit(Request $request, $id)
    {
//        $this->checkPermission('animal_update');
        $row = $this->weaponType::find($id);
        if (empty($row)) {
            return redirect(route('weapon.admin.index'));
        }
        $translation = $row->translate($request->query('lang',get_main_lang()));
        if (!$this->hasPermission('weapon_manage_others')) {
            if ($row->author_id != Auth::id()) {
                return redirect(route('weapon.admin.index'));
            }
        }
        $data = [
            'row'               => $row,
            'translation'       => $translation,
//            "selected_terms"    => $row->terms->pluck('term_id'),
//            'attributes'        => $this->attributes::where('service', 'weapon')->get(),
            'weapon_location'      => $this->location::where('status', 'publish')->get()->toTree(),
            'enable_multi_lang' => true,
            'breadcrumbs'       => [
                [
                    'name' => __('Animal'),
                    'url'  => route('weapon.admin.index')
                ],
                [
                    'name'  => __('Edit Animal'),
                    'class' => 'active'
                ],
            ],
            'page_title'        => __("Edit: :name", ['name' => $row->title])
        ];
        return view('Animals::admin.detail', $data);
    }
}
